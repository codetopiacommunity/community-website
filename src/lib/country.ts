const NAME_TO_ISO2 = new Map<string, string>();
for (const code of REGION_CODES) {
  const name = regionDisplayNames.of(code);
  if (name) NAME_TO_ISO2.set(name.toLowerCase(), code);
}

// Common names/abbreviations that don't match Intl's canonical English names.
