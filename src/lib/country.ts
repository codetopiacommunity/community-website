function isoToFlagEmoji(iso2: string): string {
  return String.fromCodePoint(
    ...[...iso2.toUpperCase()].map((char) => 127397 + char.charCodeAt(0)),
  );
}

export interface CountryFlag {
  country: string;
  flag: string;
}

/**
 * `location` is free text from the community portal (e.g. "Accra, Ghana"
 * or just "Ghana") -- there's no ISO code anywhere in the pipeline, so this
 * takes a best-effort guess from the last comma-separated segment.
 */
export function getCountryFlag(
  location: string | null | undefined,
): CountryFlag | null {
  const trimmed = location?.trim();
  if (!trimmed) return null;

  const segments = trimmed
    .split(",")
    .map((segment) => segment.trim())
    .filter(Boolean);
  const candidate = segments[segments.length - 1] ?? trimmed;
  const key = candidate.toLowerCase();
  const iso2 = NAME_TO_ISO2.get(key) ?? ALIASES[key];
  if (!iso2) return null;

  return {
    country: regionDisplayNames.of(iso2) ?? candidate,
    flag: isoToFlagEmoji(iso2),
  };
}
